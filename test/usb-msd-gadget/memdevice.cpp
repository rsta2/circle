CMemDevice::CMemDevice(size_t nsize)
{
  m_mem=static_cast<char *>(malloc(nsize));
  m_nsize=nsize;
  memset(m_mem,0,m_nsize);
  if(m_nsize>=sizeof(m_Sector0)) memcpy(m_mem,m_Sector0,sizeof(m_Sector0));
  if(m_nsize>=sizeof(m_Sector2)+2*512) memcpy(&m_mem[2*512],m_Sector2,sizeof(m_Sector2));
  if(m_nsize>= sizeof(m_Sector10)+10*512) memcpy(&m_mem[10*512],m_Sector10,sizeof(m_Sector10));
  if(m_nsize>= sizeof(m_Sector10)+22*512) memcpy(&m_mem[22*512],m_Sector10,sizeof(m_Sector10));
  if(m_nsize>= sizeof(m_Sector34)+34*512) memcpy(&m_mem[34*512],m_Sector34,sizeof(m_Sector34));
  if(m_nsize>= sizeof(m_Sector66)+66*512) memcpy(&m_mem[66*512],m_Sector66,sizeof(m_Sector66));
  if(m_nsize>= sizeof(m_Sector74)+74*512) memcpy(&m_mem[74*512],m_Sector74,sizeof(m_Sector74));  
  if(m_nsize>= sizeof(m_Sector78)+78*512) memcpy(&m_mem[78*512],m_Sector78,sizeof(m_Sector78)); 
};

CMemDevice::~CMemDevice()
{
 free(m_mem);
};


int CMemDevice::Read (void *pBuffer, size_t nCount)
{
    int readCount=nCount>(m_nsize-m_ulloffset)?(m_nsize-m_ulloffset):nCount;
    memcpy(pBuffer,(void *)(m_mem+m_ulloffset),readCount);
    return readCount;
}


int CMemDevice::Write (const void *pBuffer, size_t nCount)
{
    int writeCount=nCount>(m_nsize-m_ulloffset)?(m_nsize-m_ulloffset):nCount;
    memcpy((void *)(m_mem+m_ulloffset),pBuffer,writeCount);
    return writeCount;
}


u64 CMemDevice::Seek (u64 ullOffset)
{
    if(ullOffset>=m_nsize)return (u64)(-1);
    m_ulloffset=ullOffset;
    return m_ulloffset;

}


u64 CMemDevice::GetSize (void) const
{
  return m_nsize;
}


int CMemDevice::IOCtl (unsigned long ulCmd, void *pData)
{
 return -1;
}


boolean CMemDevice::RemoveDevice (void){
 return false;
}

